'''Python的缩进很重要！Python 中实现对代码的缩进，可以使用空格或者 Tab 键实现。
但无论是手动敲空格，还是使用 Tab 键，通常情况下都是采用 4 个空格长度作为一个缩进量（默认情况下，一个 Tab 键就表示 4 个空格）。
Python 对代码的缩进要求非常严格，同一个级别代码块的缩进量必须一样，否则解释器会报 SyntaxError 异常错误。
   快捷键：
       多行向右：tab
       多行向左：shift+tab
'''