#单行注释用#，不多介绍

#Python 使用三个连续的单引号'''或者三个连续的双引号"""注释多行内容，具体格式如下：
'''
使用 3 个单引号分别作为注释的开头和结尾
可以一次性注释多行内容
这里面的内容全部是注释内容
'''

'''
给代码添加说明是注释的基本作用，除此以外它还有另外一个实用的功能，就是用来调试程序。
举个例子，如果你觉得某段代码可能有问题，可以先把这段代码注释起来
让 Python 解释器忽略这段代码，然后再运行。如果程序可以正常执行，则可以说明错误就是由这段代码引起的；
反之，如果依然出现相同的错误，则可以说明错误不是由这段代码引起的。
在调试程序的过程中使用注释可以缩小错误所在的范围，提高调试程序的效率。
'''